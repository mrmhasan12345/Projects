            thasneema.upd_staff_cust_sup("staff",staff_id.getText(),staff_name.getText(),staff_add.getText(),staff_cont.getText(),staff_mail.getText(),"Staff_Id","Staff_Name", "Staff_Address","Staff_Mobile","Staff_Email");

