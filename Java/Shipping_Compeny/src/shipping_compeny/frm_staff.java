        public void fill_staff_tbl() throws SQLException{
    
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost/thasneema?useSSL=false","root","");
            Statement stm=con.createStatement();
            String thas="SELECT * FROM staff";
            ResultSet res=stm.executeQuery(thas);
            
            
           DefaultTableModel model=(DefaultTableModel)staff_tbl.getModel();
            
            Object[] row;
            
            while(res.next()){
            
                row = new Object[5];
                
                row[0]=res.getString("Staff_Id");
                row[1]=res.getString("Staff_Name");
                row[2]=res.getString("Staff_Address");
                row[3]=res.getString("Staff_Mobile");
                row[4]=res.getString("Staff_Email");
                
                model.addRow(row);
                
            }
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(frm_cust.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    
    }

    
    
